function reverseService(service: RouteService): RouteService {
  const duration = service.stops.at(-1)?.offsetMinutes ?? 0

  return {
    ...service,
    id: `${service.id}-reverse`,
    stops: service.stops
      .map((stop) => ({
        stationId: stop.stationId,
        offsetMinutes: duration - stop.offsetMinutes,
      }))
      .reverse(),
    firstDepartureMinutes: service.firstDepartureMinutes + 7,
    lastDepartureMinutes: service.lastDepartureMinutes + 7,
  }
}

export const routeServices: RouteService[] = [
  ...baseServices,
  ...baseServices.map(reverseService),
]
